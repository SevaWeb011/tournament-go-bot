class Tournament:
    def __init__(self):
        self.start = ""
        self.end = ""
        self.name = ""
        self.city = ""

    def setStart(self, start):
        self.start = start

    def setEnd(self, end):
        self.end = end

    def setName(self, name):
        self.name = name

    def setCity(self, city):
        self.city = city
